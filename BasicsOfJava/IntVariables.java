package BasicsOfJava;

public class IntVariables {
    public static void main(String[] args) {
        int a ;                        // a naam se ek dabba bana
        a =786 ;                       // then , uss dabbe main value store kiye
        System.out.println(a);         // Phir print kar diye value ko
        a =999 ;                       // Now ,  this is called updation value
        System.out.println(a);         // print updated value
        a = a + 10 ;
        System.out.println(a);
        int x = 5 ;
        System.out.println(x);
        x = 7 ;
        System.out.println(x);
        x = x + 6 ;
        System.out.println(x);
        x = x - 20 ;
        System.out.println(x);
    }
}
